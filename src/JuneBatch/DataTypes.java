package JuneBatch;

public class DataTypes {

	public static void main(String[] args) {
	// 1. Primitive dataTypes
		 // a. Boolean data types: true or false
		//  b. Numeric Type
		  // b.1 Character type: Char
		  // b.2 Integral types
		     	// b.2.a Integer : byte, short, int, long
				// b.2.b. Floating -Point: float, double
		
		
//Byte range: -128 to 127
		//size: 8 bits
		
		byte b = 15;
		System.out.println(b);
		
//short range: -32768 to 32767
		//size: 16 bites / 2 bytes
		
		short sh = 1000;
		System.out.println(sh);
		
		
//int range : -2147483648 to 2147483647
		//size : 32 bits/ 4 bytes
		
		int a =10;
		
		System.out.println(a);
//long size: 64 bits/ 8 bytes
		
		long l= 1000000;
		System.out.println(l);
		
		long l1 = 123654789632147l; // end we need to add 'l'
		System.out.println(l1);
		
//float size: 32 bits / 4 bytes
		// it takes 7 decimal points
		float f = 22.33f;
		System.out.println(f);
		
		float f1 = (float) 33.33;
		
		System.out.println(f1);
		
//double size: 64 bits/ 8 bytes
		//it takes 16 decimal points
		double d = 15.22547863;
		System.out.println(d);
		
		
//boolean : true or false
		boolean flag = true;
		boolean flag1 = false;
		System.out.println(flag);
		System.out.println(flag1);

//char size: 16 bites / 2 bytes
		char c = 'a';
		System.out.println(c);
		char c1 ='1';
		System.out.println(c1);
		
		


	}

}
